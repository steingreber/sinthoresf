SELECT pessoas.cod_pessoa AS cod_pessoa,
  empresas.nome_empresa,
  socios.cod_socio AS cod_socio,
  socios.socio AS socio,
  pessoas.datacadastro AS datacadastro,
  pessoas.nome AS nome,
  pessoas.endereco AS endereco,
  pessoas.numero AS numero,
  pessoas.complemento AS complemento,
  pessoas.bairro AS bairro,
  pessoas.cidade AS cidade,
  pessoas.estado AS estado,
  pessoas.CEP AS CEP,
  pessoas.telefone AS telefone,
  pessoas.sexo AS sexo,
  pessoas.datanasc AS datanasc,
  pessoas.estado_civil AS estado_civil,
  pessoas.rg AS rg,
  pessoas.cpf AS cpf,
  pessoas.carteira_trabalho AS carteira_trabalho,
  pessoas.nacionalidade AS nacionalidade,
  pessoas.naturalidade AS naturalidade,
  socios.funcao AS funcao,
  socios.dependentes AS dependentes,
  config.A09DESCRICAO AS A09DESCRICAO,
  config.A04imagem AS A04imagem,
  config.A01URL AS A01URL,
  config.A21EMPRESA AS A21EMPRESA
FROM pessoas
  JOIN socios ON pessoas.cod_pessoa = socios.socio
  INNER JOIN empresas ON empresas.cod_empresa = socios.cod_empresa,
  config
